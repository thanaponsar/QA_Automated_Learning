// Relational or comparison operators

// > - more than
// < - less than
// >= - more than or equal to
// <= - less than or equal to
   

var result = 10 > 5
console.log(result)

result = 10 < 5
console.log(result)

result = 10 >= 5
console.log(result)

result = 10 <= 5
console.log(result)

result = 10 == 5
console.log(result) 

